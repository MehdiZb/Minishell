#include "minishell.h"
#include <stdio.h>
#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	if (s)
		write(fd, s, ft_strlen(s));
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &"0123456789"[n % 10], 1);
}

int	perror_i(char *str, int i, void *ptr_one, void *ptr_two)
{
	if (ptr_one)
		xfree(ptr_one);
	if (ptr_two)
		xfree(ptr_two);
	perror(str);
	return (i);
}
